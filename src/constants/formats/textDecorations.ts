/**
 * @desc ANSI text decorations
 * @readonly
 * @enum {string}
 */
export enum textDecorations {
    /**
     * @desc Bold font decoration
     * @default textDecorations.BOLD = '1'
     */
    BOLD = '1',

    /**
     * @desc Dim font decoration
     * @default textDecorations.DIM = '2'
     */
    DIM = '2',
    
    /**
     * @desc Italic font decoration
     * @default textDecorations.ITALIC = '3'
     */
    ITALIC = '3',

    /**
     * @desc Underline font decoration
     * @default textDecorations.UNDERLINE = '4'
     */
    UNDERLINE = '4',

    /**
     * @desc Strikethrough font decoration
     * @default textDecorations.STRIKETHROUGH = '9'
     */
    STRIKETHROUGH = '9',

    /**
     * @desc Fractur font decoration
     * @default textDecorations.FRACTUR = '20'
     */
    FRACTUR = '20',

    /**
     * @desc Underline double font decoration
     * @default textDecorations.UNDERLINE_DOUBLE = '21'
     */
    UNDERLINE_DOUBLE = '21',

    /**
     * @desc Reset bold font decoration
     * @default textDecorations.BOLD_RESET = '22'
     */
    BOLD_RESET = '22',

    /**
     * @desc Reset dim font decoration
     * @default textDecorations.DIM_RESET = '22'
     */
    DIM_RESET = '22',

    /**
     * @desc Reset italic font decoration
     * @default textDecorations.ITALIC_RESET = '23'
     */
    ITALIC_RESET = '23',

    /**
     * @desc Reset underline font decoration
     * @default textDecorations.UNDERLINE_RESET = '24'
     */
    UNDERLINE_RESET = '24',

    /**
     * @desc Proportional spacing font decoration
     * @default textDecorations.PROPORTINAL_SPACING = '26'
     */
    PROPORTINAL_SPACING = '26',
    
    /**
     * @desc Reset strikethrough font decoration
     * @default textDecorations.STRIKETHROUGH_RESET = '29'
     */
    STRIKETHROUGH_RESET = '29',

    /**
     * @desc Reset proportional spacing font decoration
     * @default textDecorations.PROPORTINAL_SPACING_RESET = '50'
     */
    PROPORTINAL_SPACING_RESET = '50',

    /**
     * @desc Overline font decoration
     * @default textDecorations.OVERLINE = '53'
     */
    OVERLINE = '53',

    /**
     * @desc Reset overline font decoration
     * @default textDecorations.OVERLINE_RESET = '55'
     */
    OVERLINE_RESET = '55',

    /**
     * @desc Underline color font decoration
     * @default textDecorations.UNDERLINE_COLOR = '58'
     */
    UNDERLINE_COLOR = '58',

    /**
     * @desc Reset underline color font decoration
     * @default textDecorations.UNDERLINE_COLOR_RESET = '59'
     */
    UNDERLINE_COLOR_RESET = '59'
}
