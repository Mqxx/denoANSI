/**
 * @desc ANSI format ending sequences
 * @readonly
 * @enum {string}
 */
export enum formatsEnds {
    /**
     * @desc Color ending sequence
     * @default formats.COLOR = 'm'
     */
    COLOR = 'm',

    /**
     * @desc Format ending sequence
     * @default formats.FORMAT = 'm'
     */
    FORMAT = 'm'
}
